function event_spawn(e)
	if(e.other:Class() == "Shaman") then
		eq.attack(" .. e.other:GetName() .. ");
	end
end

function event_trade(e)
	local item_lib = require("items");
	item_lib.return_items(e.self, e.other, e.trade)
end

